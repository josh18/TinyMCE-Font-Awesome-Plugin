    // Event to watch node changes.
    // If cursor is in a span.fa and user types something, move the new content 
    // behind the span to ensure separated icons (and prevent text in .fa-spans)
    editor.on('NodeChange', function(e) {

        if( e.element.nodeName == 'SPAN' && e.element.className.indexOf('fa') >= 0 ) {

            if( e.element.innerHTML.length > 1 ) { // More than one character (not just an icon, as it should be)

                var fontAwesomeStart = 0xF000,
                    firstChar = e.element.innerHTML[0],
                    lastChar = e.element.innerHTML[e.element.innerHTML.length - 1],
                    content, contentNode;

                if( lastChar.charCodeAt() >= fontAwesomeStart ) { // Last character is an icon (presumably another icon is inserted before)
                    content = e.element.innerHTML.substr(0, e.element.innerHTML.length - 1); // Everything from begin to last character
                    e.element.insertAdjacentHTML('beforeBegin', content); // Move new content before icon
                    e.element.innerHTML = lastChar; // Last character only (the icon)
                    contentNode = e.element.previousSibling; // The newly inserted content
                } else { // Something is inserted after the icon
                    content = e.element.innerHTML.substr(1); // Everything from first position to the end
                    e.element.insertAdjacentHTML('afterEnd', content); // Move new content to the end
                    e.element.innerHTML = firstChar; // First character only (the icon)
                    contentNode = e.element.nextSibling; // The newly inserted content
                }

                // innerHTML could fail on text-only nodes, use content-length in that case
                editor.selection.setCursorLocation(contentNode, (contentNode.innerHTML ? contentNode.innerHTML.length : content.length)); // Move the cursor at the end

            } else if( e.element.innerHTML.length == 0 ) { // <span> is empty
                e.element.parentNode.removeChild(e.element); // Remove the node
            }

            if( e.element.parentNode && e.element.parentNode.nodeName == 'SPAN' && e.element.parentNode.className.indexOf('fa') >= 0 ) {
                // FontAwesome-<span> is inside another FontAwesome-<span>, repair that.
                // (happens when an icon is inserted directly after another just inserted icon which is followed by another icon).
                var parent = e.element.parentNode;
                parent.parentNode.insertBefore(e.element, parent.nextSibling);
                // Move the cursor behind the new icon
                editor.selection.setCursorLocation(e.element, (e.element.innerHTML ? e.element.innerHTML.length : 0)); // Move the cursor at the end
            }


        }
    });

    // Called when editor is loaded or plain HTML is saved.
    // Replaces the <i>-tags with <span>'s to provide a natural editing experience
    editor.on('BeforeSetContent', function(e) {

        e.content = e.content.replace(/<i\s*(style=["'](?:[^"']*)["'])?\s*class=["'][^"']*(fa-[a-z0-9\-]*)[^"']*["']\s*(style=["'](?:[^"']*)["'])?[^>]*>[^>]*<\/i>/g, function(match, styleAttr1, iconClass, styleAttr2) {
            // Find the belonging charcode
            for( var group in iconset ) { // Loop through groups
                for( var i = 0; i < iconset[group].length; i++ ) { // Loop through icons
                    for( var j = 0; j < iconset[group][i].aliases.length; j++ ) { // Loop through aliases
                        if( iconset[group][i].aliases[j] == iconClass ) { // Icon found
                            // Support for multiple FontAwesome classes (fa-spin, fa-pulse, etc.)
                            var additionalClasses = /fa-[a-z0-9\-]*/.exec( match.replace(new RegExp(iconClass, 'g'), '') );
                            return '<span ' + (styleAttr1 ? styleAttr1 + ' ' : '') + (styleAttr2 ? styleAttr2 + ' ' : '') + 'class="fa ' + (additionalClasses ? additionalClasses.join(' ') : '') + '">' + iconset[group][i].code + '</span>';
                        }
                    }
                }
            }
            return match; // Fallback if no replacement is found (return un-replaced)
        });

    });


    // Called before save (or on .getContent()).
    // Replaces the <span>'s with <i> to get valid markup without strange Unicode characters
    editor.on('GetContent', function(e) {

        e.content = e.content.replace(/<span\s*(style=["'](?:[^"']*)["'])?\s*class=["']fa\s?((?:fa-[a-z0-9\-]*\s?)*)["']\s*(style=["'](?:[^"']*)["'])?[^>]*>([^<]*)<\/span>/g, function(match, styleAttr1, additionalClasses, styleAttr2, charcode) {
        
            if( charcode.length == 1 ) // Charcode has to be html-encoded first.
                charcode = '&#x' + charcode.charCodeAt(0).toString(16).toLowerCase() + ';'; // charCodeAt returns decimal values - convert to hex
            else // Convert decimal values to hex
                charcode = charcode.replace(/&#([0-9]+);/, function(match, decimal) { 
                    return '&#x' + (+decimal).toString(16).toLowerCase() + ';';
                });

            // Find the belonging class
            for( var group in iconset ) { // Loop through groups
                for( var i = 0; i < iconset[group].length; i++ ) { // Loop through icons
                    if( iconset[group][i].code == charcode ) { // Icon found
                        return '<i class="fa ' + iconset[group][i].aliases[0] + (additionalClasses ? ' ' + additionalClasses : '') + '"' + (styleAttr1 ? ' ' + styleAttr1 : '') + (styleAttr2 ? ' ' + styleAttr2 : '') + '>' + (!e.source_view ? '&zwnj;' : '') + '</i>'; // Include a zero-width non-joiner to prevent removing of empty tags 
                    }
                }
            }
            return match; // Fallback if no replacement is found (return un-replaced)
        });

    });

